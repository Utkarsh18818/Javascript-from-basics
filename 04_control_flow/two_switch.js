// Switch case statement 
const month = "dec";

switch (month) {
    case "jan":
        console.log("month is january");
        break;
    case "fab":
        console.log("month is fabuary");
        break;
    case "march":
        console.log("month is March");
        break;
    case "april":
        console.log("month is April");
        break;

    default:
        console.log("default case");
        break;
}